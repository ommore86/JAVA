public class CWH_forLoop {
    public static void main(String[] args) {

        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }

        // Odd numbers
        int n = 3;
        for (int i = 0; i < n; i++) {
            System.out.println(2 * i + 1);
        }
    }
}